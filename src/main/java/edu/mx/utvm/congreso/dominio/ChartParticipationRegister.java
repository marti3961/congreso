package edu.mx.utvm.congreso.dominio;

public class ChartParticipationRegister extends ChartData{
	private int numberOfParticipants;
	private int numberOfAcademicBodies;
	public int getNumberOfParticipants() {
		return numberOfParticipants;
	}
	public void setNumberOfParticipants(int numberOfParticipants) {
		this.numberOfParticipants = numberOfParticipants;
	}
	public int getNumberOfAcademicBodies() {
		return numberOfAcademicBodies;
	}
	public void setNumberOfAcademicBodies(int numberOfAcademicBodies) {
		this.numberOfAcademicBodies = numberOfAcademicBodies;
	}

}
